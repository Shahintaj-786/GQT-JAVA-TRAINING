package com.gqt.pattern;

public class Pattern_1 {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
		System.out.print(" * "); /// *	* 	*	*	*
		}
		
		System.out.println();
		for(int i =1;i<=5;i++) {
			System.out.print(i+" "); // 1	2	3	4	5
		}
		System.out.println();
		for(int i =1;i<=5;i++) {
			System.out.print( 1+" " ); // 1		1	1	1	1
		}
		

	}

}
