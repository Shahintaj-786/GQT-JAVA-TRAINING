//strings can be created using two ways
//using literals
//using objects

package com.gqt.string;

public class Creation_of_Strings {

	public static void main(String[] args) {
		
		//using literals
		
		String s1 = "Ramayana";
		System.out.println(s1);
		
		//using object 
		
		String s2 = new String("Mahabharath");
		
		System.out.println(s2);

	}

}
